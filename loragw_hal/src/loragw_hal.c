int count_one_16(uint16_t a) {
    uint16_t i;
    int count = 0;
    for (i=1; i != 0; i <<= 1) {
        if ((a & i) != 0)
            ++ count;
    }
    return count;
}


int load_firmware(uint8_t target, uint16_t *firmware, uint16_t size) {
    int i;
    int32_t read_value;
    
    /* check parameters */
    CHECK_NULL(firmware);
    if (target == PIC_AGC) {
        if (size != PIC_AGC_FW_SIZE) {
            DEBUG_MSG("ERROR: NOT A VALID SIZE FOR FIRMWARE");
            return -1;
        }
    } else if (target == PIC_ARBITER) {
        if (size != PIC_ARB_FW_SIZE) {
            DEBUG_MSG("ERROR: NOT A VALID SIZE FOR FIRMWARE");
            return -1;
        }
    } else {
        DEBUG_MSG("ERROR: NOT A VALID TARGET FOR LOADING FIRMWARE");
        return -1;
    }
    
    /* reset the targetted PIC */
    lgw_reg_r(LGW_PIC16F84_RST, &read_value);
    lgw_reg_w(LGW_PIC16F84_RST, read_value & (1 << target));
    
    /* set mux to access PIC program RAM and set address to 0 */
    lgw_reg_w(LGW_PIC16F84_SELECT_MUX, 1 << target);
    lgw_reg_w(LGW_PIC_PROM_ADDR, 0);
    
    return 0;
}

